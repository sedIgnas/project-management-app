  {
    $inGroup = $request->input('groupId') ? 1 : 0;
    $student = new Student([
      'first_name' => $request->input('firstName'),
      'last_name' => $request->input('lastName'),
      'in_group' => $inGroup,
      'group_id' => $request->input('groupId')
    ]);
    $student->save();
    return redirect(route('project.index'));
  }

